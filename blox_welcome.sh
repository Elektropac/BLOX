#!/bin/bash
# BLOX Welcome Screen Script

# Clear screen
printf "\033c"

# Set colors
GREEN='\033[0;32m'
NC='\033[0m' # No Color

# Print BLOX ASCII Art
echo -e "${GREEN}"
echo " .----------------.  .----------------.  .----------------.  .----------------. "
echo "| .--------------. || .--------------. || .--------------. || .--------------. |"
echo "| |   ______     | || |   _____      | || |     ____     | || |  ____  ____  | |"
echo "| |  |_   _ \    | || |  |_   _|     | || |   .'    `.   | || | |_  _||_  _| | |"
echo "| |    | |_) |   | || |    | |       | || |  /  .--.  \  | || |   \ \  / /   | |"
echo "| |    |  __'.   | || |    | |   _   | || |  | |    | |  | || |    > `' <    | |"
echo "| |   _| |__) |  | || |   _| |__/ |  | || |  \  `--'  /  | || |  _/ /'`\ \_  | |"
echo "| |  |_______/   | || |  |________|  | || |   `.____.'   | || | |____||____| | |"
echo "| |              | || |              | || |              | || |              | |"
echo "| '--------------' || '--------------' || '--------------' || '--------------' |"
echo " '----------------'  '----------------'  '----------------'  '----------------' "
echo -e "${NC}"

# Print system information
echo "Hostname : $(hostname)"
echo "IP Addr  : $(hostname -I | awk '{print $1}')"
echo "Uptime   : $(uptime -p)"
echo "Tailscale: $(tailscale ip -4 | head -n 1)"
echo ""


